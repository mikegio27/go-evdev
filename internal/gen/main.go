// Command gen parses the Linux kernel input headers and emits codes.go, a
// committed file containing typed event-code constants and name lookup tables.
//
// It is run via `go generate` from the package root and reads:
//
//	/usr/include/linux/input-event-codes.h  (EV_*, KEY_*, BTN_*, REL_*, ABS_*, ...)
//	/usr/include/linux/input.h              (BUS_*)
//
// Consumers of the evdev package never need the kernel headers: codes.go is
// checked in and is the source of truth.
package main

import (
	"bufio"
	"fmt"
	"go/format"
	"os"
	"regexp"
	"sort"
	"strconv"
	"strings"
)

const (
	eventCodesHeader = "/usr/include/linux/input-event-codes.h"
	inputHeader      = "/usr/include/linux/input.h"
	outFile          = "codes.go"
)

// define is one resolved `#define NAME VALUE` from a header, in file order.
type define struct {
	name  string
	value uint16
}

// prefixType maps a constant-name prefix to the EvType whose code space it
// belongs to. KEY_ and BTN_ share EV_KEY.
var prefixType = map[string]string{
	"SYN_": "EV_SYN",
	"KEY_": "EV_KEY",
	"BTN_": "EV_KEY",
	"REL_": "EV_REL",
	"ABS_": "EV_ABS",
	"MSC_": "EV_MSC",
	"SW_":  "EV_SW",
	"LED_": "EV_LED",
	"SND_": "EV_SND",
	"REP_": "EV_REP",
}

var defineRe = regexp.MustCompile(`^#define\s+([A-Z][A-Z0-9_]*)\s+(.+?)\s*(?:/\*.*)?$`)

func main() {
	if err := run(); err != nil {
		fmt.Fprintln(os.Stderr, "gen:", err)
		os.Exit(1)
	}
}

func run() error {
	syms := map[string]uint16{}      // every resolved symbol, for reference resolution
	var order []define               // defines we care about, in file order
	codePrefixes := codePrefixList() // KEY_, BTN_, REL_, ...

	collect := func(path string, want func(name string) bool) error {
		defs, err := parseHeader(path, syms)
		if err != nil {
			return err
		}
		for _, d := range defs {
			if want(d.name) {
				order = append(order, d)
			}
		}
		return nil
	}

	wantCodes := func(name string) bool {
		if strings.HasPrefix(name, "EV_") || strings.HasPrefix(name, "INPUT_PROP_") {
			return true
		}
		for _, p := range codePrefixes {
			if strings.HasPrefix(name, p) {
				return true
			}
		}
		return false
	}
	if err := collect(eventCodesHeader, wantCodes); err != nil {
		return err
	}
	if err := collect(inputHeader, func(name string) bool {
		return strings.HasPrefix(name, "BUS_")
	}); err != nil {
		return err
	}

	src := emit(order)
	formatted, err := format.Source([]byte(src))
	if err != nil {
		return fmt.Errorf("gofmt generated source: %w\n%s", err, src)
	}
	return os.WriteFile(outFile, formatted, 0o644)
}

// parseHeader reads a header, resolving each #define value against syms (which
// accumulates across calls so later headers can reference earlier symbols).
func parseHeader(path string, syms map[string]uint16) ([]define, error) {
	f, err := os.Open(path)
	if err != nil {
		return nil, err
	}
	defer f.Close()

	var defs []define
	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		m := defineRe.FindStringSubmatch(scanner.Text())
		if m == nil {
			continue
		}
		name, rawVal := m[1], strings.TrimSpace(m[2])
		val, ok := resolve(rawVal, syms)
		if !ok {
			continue // value we don't understand (struct sizes, ioctls, etc.)
		}
		syms[name] = val
		defs = append(defs, define{name: name, value: val})
	}
	return defs, scanner.Err()
}

// resolve evaluates a #define value: a hex/decimal literal, a bare symbol
// reference, or a `SYMBOL+N` / `(SYMBOL + N)` expression.
func resolve(expr string, syms map[string]uint16) (uint16, bool) {
	expr = strings.TrimSpace(strings.Trim(strings.TrimSpace(expr), "()"))
	if expr == "" {
		return 0, false
	}
	if plus := strings.IndexByte(expr, '+'); plus >= 0 {
		left, ok := resolve(expr[:plus], syms)
		if !ok {
			return 0, false
		}
		right, ok := resolve(expr[plus+1:], syms)
		if !ok {
			return 0, false
		}
		return left + right, true
	}
	if n, err := strconv.ParseUint(expr, 0, 16); err == nil {
		return uint16(n), true
	}
	if v, ok := syms[expr]; ok {
		return v, true
	}
	return 0, false
}

func codePrefixList() []string {
	out := make([]string, 0, len(prefixType))
	for p := range prefixType {
		out = append(out, p)
	}
	sort.Strings(out)
	return out
}

func isMaxOrCnt(name string) bool {
	return strings.HasSuffix(name, "_MAX") || strings.HasSuffix(name, "_CNT")
}

func emit(defs []define) string {
	var b strings.Builder
	p := func(format string, a ...any) { fmt.Fprintf(&b, format, a...) }

	p("// Code generated by internal/gen; DO NOT EDIT.\n\n")
	p("package evdev\n\n")

	p("// EvType is an evdev event type (the EV_* constants).\n")
	p("type EvType uint16\n\n")
	p("// EvCode is an evdev event code within a type's namespace (KEY_*, REL_*, ...).\n")
	p("type EvCode uint16\n\n")
	p("// BusType identifies the bus a device is attached to (the BUS_* constants).\n")
	p("type BusType uint16\n\n")
	p("// InputProp is a device property (the INPUT_PROP_* constants).\n")
	p("type InputProp uint16\n\n")

	// Group defines for typed const blocks and lookup tables.
	var evTypes, props, buses []define
	codesByType := map[string][]define{} // EvType name -> code defines
	var allCodes []define                // for name<->code maps (excludes MAX/CNT)

	for _, d := range defs {
		switch {
		case strings.HasPrefix(d.name, "EV_"):
			evTypes = append(evTypes, d)
		case strings.HasPrefix(d.name, "INPUT_PROP_"):
			props = append(props, d)
		case strings.HasPrefix(d.name, "BUS_"):
			buses = append(buses, d)
		default:
			for p, evType := range prefixType {
				if strings.HasPrefix(d.name, p) {
					codesByType[evType] = append(codesByType[evType], d)
					if !isMaxOrCnt(d.name) {
						allCodes = append(allCodes, d)
					}
					break
				}
			}
		}
	}

	emitConstBlock(p, "EvType", evTypes)
	emitConstBlock(p, "EvCode", flatten(codesByType))
	emitConstBlock(p, "BusType", buses)
	emitConstBlock(p, "InputProp", props)

	// evTypeNames: EvType -> "EV_KEY"
	p("var evTypeNames = map[EvType]string{\n")
	for _, d := range evTypes {
		if isMaxOrCnt(d.name) {
			continue
		}
		p("\t%s: %q,\n", d.name, d.name)
	}
	p("}\n\n")

	// evTypeByName: "EV_KEY" -> EvType
	p("var evTypeByName = map[string]EvType{\n")
	for _, d := range evTypes {
		if isMaxOrCnt(d.name) {
			continue
		}
		p("\t%q: %s,\n", d.name, d.name)
	}
	p("}\n\n")

	// evCodeNames: EvType -> (EvCode -> canonical name). First name wins so
	// aliases (e.g. BTN_LEFT == BTN_MOUSE) resolve to the first-defined name.
	p("var evCodeNames = map[EvType]map[EvCode]string{\n")
	for _, evType := range sortedKeys(codesByType) {
		p("\t%s: {\n", evType)
		seen := map[uint16]bool{}
		for _, d := range codesByType[evType] {
			if isMaxOrCnt(d.name) || seen[d.value] {
				continue
			}
			seen[d.value] = true
			p("\t\t0x%x: %q,\n", d.value, d.name)
		}
		p("\t},\n")
	}
	p("}\n\n")

	// evCodeByName: every code/alias name -> EvCode
	p("var evCodeByName = map[string]EvCode{\n")
	for _, d := range allCodes {
		p("\t%q: 0x%x,\n", d.name, d.value)
	}
	p("}\n\n")

	// busNames: BusType -> "BUS_USB"
	p("var busNames = map[BusType]string{\n")
	for _, d := range buses {
		p("\t%s: %q,\n", d.name, d.name)
	}
	p("}\n\n")

	// propNames: InputProp -> "INPUT_PROP_POINTER"
	p("var propNames = map[InputProp]string{\n")
	for _, d := range props {
		if isMaxOrCnt(d.name) {
			continue
		}
		p("\t%s: %q,\n", d.name, d.name)
	}
	p("}\n\n")

	return b.String()
}

// emitConstBlock writes a typed const block, skipping duplicate values' names
// only where Go would object (it won't — duplicate values are legal), so we
// keep every name for discoverability.
func emitConstBlock(p func(string, ...any), typ string, defs []define) {
	if len(defs) == 0 {
		return
	}
	p("const (\n")
	for _, d := range defs {
		p("\t%s %s = 0x%x\n", d.name, typ, d.value)
	}
	p(")\n\n")
}

func flatten(m map[string][]define) []define {
	var out []define
	for _, evType := range sortedKeys(m) {
		out = append(out, m[evType]...)
	}
	return out
}

func sortedKeys(m map[string][]define) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}
